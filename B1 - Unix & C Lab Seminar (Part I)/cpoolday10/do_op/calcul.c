int my_add(int i, int j)
{
    int result = i + j;
    return result;
}

int my_sub(int i, int j)
{
    int result = i - j;
    return result;
}

int my_div(int i, int j)
{
    int result = i / j;
    return result;
}

int my_mul(int i, int j)
{
    int result = i * j;
    return result;
}

int my_mod(int i, int j)
{
    int result = i % j;
    return result;
}

